// if statement
// using if statement we chcek a given condion is satisfied of or not
// if condition satisfied the code block gets executed
// basically if condition is true then code block of if executed; and if condition false flow of programme is out from if block

const myAge = 20;

if (myAge > 18) {
  console.log("You are eligible for voter card");
}

// if-else statement
// there two blocks are created
// 1. if block; 2. else block
// if given condition is true then statement of if block gets executed; and if condition is false then statement of else block gets executed
// at a time on any given condition only one block of code gets executed

let studentMarks = 50;

if (studentMarks > 60) {
  console.log("You are pass in exam");
} else {
  console.log("You are fail in exam");
}
