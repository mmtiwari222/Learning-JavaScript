// switch statement

// swith case is also use for decision making purpose

//  given expression in swith () is meets from which case the statement of that case is executed.

let month = 3;

switch (month) {
  case 1:
    console.log("Jan");
    break;
  case 2:
    console.log("Feb");
    break;
  case 3:
    console.log("March");
    break;
  default:
    console.log("Month");

    break;
}

// break keyword break the flow when the switch expression meets.
// if any case not meets then defalut gets executed
